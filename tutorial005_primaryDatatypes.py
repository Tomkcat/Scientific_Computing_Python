'''

Primary datatypes

'''

# Float/Real
a = 1.5
print(a, id(a), type(a))

# Integer
a = 5
print(a, id(a), type(a))

# Complex
a = 6.0+7.8j
print(a, id(a), type(a))

# Logical
a = True
print(a, id(a), type(a))

# String/Character
a = "hello"
print(a, id(a), type(a))

# Get the ASCII/unicode value of a character
print(ord('z'))

# Get the character for the ASCII/unicode
print(chr(98))
print(chr(32250))