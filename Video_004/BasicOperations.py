"""
Program to test all Basic Operations
"""

a = 11
b = 2

print("a = ", a, " and b = ", b)

# Addition
print("a + b = ", a + b)
# Subtraction
print("a - b = ", a - b)
# Multiplication
print("a * b = ", a * b)
# Division
print("a / b = ", a / b)
# Floor Division
print("a // b = ", a // b)
# Modulus
print("a % b = ", a % b)
# Exponentiation
print("a ** b = ", a ** b)